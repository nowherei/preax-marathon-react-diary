export function formatDate(date, type = "short") {

  const months = [
    "—è–Ω–≤–∞—Ä—è",
    "—Ñ–µ–≤—Ä–∞–ª—è",
    "–º–∞—Ä—Ç–∞",
    "–∞–ø—Ä–µ–ª—è",
    "–º–∞—è",
    "–∏—é–Ω—è",
    "–∏—é–ª—è",
    "–∞–≤–≥—É—Å—Ç–∞",
    "—Å–µ–Ω—Ç—è–±—Ä—è",
    "–Ω–æ—è–±—Ä—è",
    "–¥–µ–∫–∞–±—Ä—è",
  ];

  let res = date.getDate();
  const currentMonth = months[date.getMonth()];
  if (type === "short") {
    res += ` ${currentMonth.slice(0, 3)}`;
  }

  if (type === "long") {
    res += ` ${currentMonth} ${date.getFullYear()} –≥–æ–¥–∞`;
  }

  return res;
}
